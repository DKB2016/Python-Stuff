for i in range(1,13):
    # when you use a colon(:) you automatically get a new code block(indentation = 4 spaces)
    print("No. {} squared is {} and cubed is {:4}".format(i, i ** 2, i ** 3))
    # you can also use tab to create a new code block
    # code at the same indentation are a part of the same code block
    print("*" * 80)
    
